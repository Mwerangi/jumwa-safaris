                </p>
                <p>
                    Ngorongoro Crater is the largest unbroken caldera in the world. The cone is believed to have collapsed nearly two million years ago, and while the cone was standing, it could have been as tall, if not taller, than Mt. Kilimanjaro. The crater (1,800 feet deep and 102 square miles) is a microcosm of East African scenery with abundant wildlife – 
                    more than 25,000 larger animals, mostly zebra and wildebeest. This is also the best place in Tanzania to see magnificent black-maned lion and black rhino. A soda lake changes size depending on the rains that are the lifeline of this amazing land.
                </p>
                <p>
                    Colorful flamingoes and more than 100 species of birds found here and not in the Serengeti, frequent the area. Patience may also reward you with glimpses of leopard, cheetah, hyena, jackal, elephant, warthog, buffalo, impala, buffalo, hartebeest, eland and lots of other antelopes. As you descend into the crater, you may see the local Maasai 
                    as they take their cattle down the steep walls of the crater.
                </p>
