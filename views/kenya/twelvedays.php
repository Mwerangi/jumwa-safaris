                </p>
                <p>
                    Serengeti National Park (3,646,500 acres) is the jewel in the crown of Tanzania’s protected areas with over three million large mammals roaming its plains. The Serengeti has come to symbolize paradise to all, including the local Maasai, who have grazed their cattle on the vast grassy plains for generations. To the Maasai, it is Siringitu – "the place where the land goes on forever." 
                    Today, the Serengeti National Park, the Ngorongoro Conservation Area, and the Maasai Mara Game Reserve across the border in Kenya, protect the greatest and most varied collection of terrestrial wildlife on earth, and one of the last great migratory systems still intact.
                </p>
                <p>
                    The Singita Faru Faru Lodge is situated on a slope alongside the riverbank in the Grumeti Reserves .The exclusive, luxury accommodation on offer at Faru Faru Lodge is surrounded by a variety of animal species and birdlife.Two rim-flow swimming pools and a large viewing deck overlook the watering hole where the animals come to visit while a Swahili-inspired beach offers the ideal place for some serious rest and relaxation.
