                <p>The reserve harbours a number of wildlife species rarely found elsewhere in any numbers, such as Grevy's zebra, the reticulated giraffe and the Beisa Oryx. Samburu's permanent water supply is a magnet for numerous animal species that include the big cats, herbivores and reptiles such as crocodiles. The long-necked gerenuk, elephants, cheetah, buffalos, Grant's gazelle, 
